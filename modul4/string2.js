// (double quotes)
let coba = "Hello, Gerry Disini";
console.log(coba); // Output: Hello, Gerry Disini

// (single quotes)
let a = "Lagi pusing kawan";
console.log(a); // Output: Lagi pusing kawan

// String dengan tanda kutip ganda di dalam string yang menggunakan tanda kutip satu
let b = 'Dua kata lucu, "Somay tikus"';
console.log(b); // Output: Dua kata lucu, "Somay tikus"

// String dengan tanda kutip satu di dalam string yang menggunakan tanda kutip ganda
let c = "Jangan lupa apa?, 'Makan dong.'";
console.log(c); // Output: Jangan lupa apa? 'Makan dong.'
